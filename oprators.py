# Logical Operator:-
# and,or,not
# and-->if both argument are True then only result is True
# or -->if atleast one argument is True then the result is True
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print(True or True)
print(True or False)
print(False or True)
print(False or False)

